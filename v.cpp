#include <iostream>
using namespace std;

int main() {
    int myInt = 5;
    float myFloat = 5.99;
    double myDouble = 9.98;
    char myChar = 'D';
    bool myBoolean = true;
    string myString = "Hello";

    /* output */
    cout << "int: " << myInt << "\n";
    cout << "float: " << myFloat << "\n";
    cout << "double: " << myDouble << "\n";
    cout << "char: " << myChar << "\n";
    cout << "bool: " << myBoolean << "\n";
    cout << "string: " << myString;
    return 0;
}